package com.jacdev.picplacerest.utils;

public class PhotoDimensions {

	private int width, height;
	public PhotoDimensions(int width, int height) {
		this.width = width;
		this.height = height;
	}
	
	public int getWidth() {
		return this.width;
	}
	public int getHeight() {
		return this.height;
	}

}
